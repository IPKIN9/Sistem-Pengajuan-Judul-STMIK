<?php

namespace App\Repositories;

use App\Interfaces\AdminRepositoryInterface;
use App\Models\AdminModel;
use Carbon\Carbon;

class AdminRepository implements AdminRepositoryInterface
{
    public function getAllAdmin()
    {
        try {
            $admin = AdminModel::all();
        } catch (\Throwable $th) {
            $admin = $th->getMessage();
        }

        return $admin;
    }

    public function getAdminById($admin_id)
    {
        try {
            $dbResult = AdminModel::whereId($admin_id)->first();
            if ($dbResult) {
                $admin = array(
                    'data' => $dbResult,
                    'response' => array(
                        'icon' => 'success',
                        'title' => 'Ditemukan',
                        'message' => 'Data berhasil ditemukan',
                    ),
                    'code' => 201
                );
            } else {
                $admin = array(
                    'data' => null,
                    'response' => array(
                        'icon' => 'warning',
                        'title' => 'Not Found',
                        'message' => 'Data tidak tersedia',
                    ),
                    'code' => 404
                );
            }
        } catch (\Throwable $th) {
            $admin = array(
                'data' => null,
                'response' => array(
                    'icon' => 'error',
                    'title' => 'Gagal',
                    'message' => $th->getMessage(),
                ),
                'code' => 500
            );
        }

        return $admin;
    }

    public function createAdmin(array $adminDetails)
    {
        try {
            $dbResult = AdminModel::create($adminDetails);
            $admin = array(
                'data' => $dbResult,
                'response' => array(
                    'icon' => 'success',
                    'title' => 'Tersimpan',
                    'message' => 'Data berhasil disimpan',
                ),
                'code' => 201
            );
        } catch (\Throwable $th) {
            $admin = array(
                'data' => null,
                'response' => array(
                    'icon' => 'error',
                    'title' => 'Gagal',
                    'message' => $th->getMessage(),
                ),
                'code' => 500
            );
        }

        return $admin;
    }

    public function updateAdmin($admin_id, array $newDetails)
    {
        $date = Carbon::now();
        $newDetails['updated_at'] = $date;

        try {
            $dbResult = AdminModel::whereId($admin_id);
            $findId = $dbResult->first();
            if ($findId) {
                $admin = array(
                    'data' => $dbResult->update($newDetails),
                    'response' => array(
                        'icon' => 'success',
                        'title' => 'Tersimpan',
                        'message' => 'Data berhasil diperbaharui',
                    ),
                    'code' => 201
                );
            } else {
                $admin = array(
                    'data' => null,
                    'response' => array(
                        'icon' => 'warning',
                        'title' => 'Not Found',
                        'message' => 'Data tidak tersedia',
                    ),
                    'code' => 404
                );
            }
        } catch (\Throwable $th) {
            $admin = array(
                'data' => null,
                'response' => array(
                    'icon' => 'error',
                    'title' => 'Gagal',
                    'message' => $th->getMessage(),
                ),
                'code' => 500
            );
        }

        return $admin;
    }

    public function deleteAdmin($admin_id)
    {
        try {
            $dbResult = AdminModel::whereId($admin_id);
            $findId = $dbResult->first();
            if ($findId) {
                $admin = array(
                    'data' => $dbResult->delete(),
                    'response' => array(
                        'icon' => 'success',
                        'title' => 'Terhapus',
                        'message' => 'Data berhasil dihapus',
                    ),
                    'code' => 201
                );
            } else {
                $admin = array(
                    'data' => null,
                    'response' => array(
                        'icon' => 'warning',
                        'title' => 'Not Found',
                        'message' => 'Data tidak tersedia',
                    ),
                    'code' => 404
                );
            }
        } catch (\Throwable $th) {
            $admin = array(
                'data' => null,
                'response' => array(
                    'icon' => 'error',
                    'title' => 'Gagal',
                    'message' => $th->getMessage(),
                ),
                'code' => 500
            );
        }

        return $admin;
    }
}
